'''PROMEDIO DE NOTAS
Un alumno tiene 3 notas: 7, 8 y 9.
Calcular el promedio de las mismas y mostrar el resultado.
'''
nota1 = int(input("Ingresar nota 1: "))
nota2 = int(input("Ingresar nota 2: "))
nota3 = int(input("Ingresar nota 3: "))
totalNotas = nota1 + nota2 + nota3
promedio = totalNotas/3
print("El promedio es : ",promedio)
